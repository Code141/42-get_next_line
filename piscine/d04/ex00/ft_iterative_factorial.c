int	ft_iterative_factorial(int nb)
{
	int r;

	if (nb < 0 || nb > 12)
		return (0);
	if (nb < 2)
		return (1);
	r = 1;
	while (nb > 0)
	{
		r *= nb;
		nb--;
	}
	return (r);
}
