int	nmatch(char *s1, char *s2)
{
	int i;

	i = 0;
	if (!*s1 && *s2 == '*')
		return (nmatch(s1, s2 + 1));
	if (*s2 == '*' && *s1)
	{
		i += nmatch(s1, s2 + 1);
		i += nmatch(s1 + 1, s2);
		return (i);
	}
	if (*s1 == *s2 && *s1 && *s2)
		return (nmatch(s1 + 1, s2 + 1));
	if (!*s1 && !*s2)
		return (1);
	return (0);
}
