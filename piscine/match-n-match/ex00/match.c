int	match(char *s1, char *s2)
{
	int i;

	i = 0;
	if (!*s1 && *s2 == '*')
		return (match(s1, s2 + 1));
	if (*s2 == '*' && *s1)
	{
		i += match(s1, s2 + 1);
		i += match(s1 + 1, s2);
		i = (i != 0) ? 1 : 0;
		return (i);
	}
	if (*s1 == *s2 && *s1 && *s2)
		return (match(s1 + 1, s2 + 1));
	if (!*s1 && !*s2)
		return (1);
	return (0);
}
