#include "sudoku.h"

int	ft_input_check(char **tab)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (x < 9)
	{
		y = 0;
		while (tab[x][y])
		{
			if ((tab[x][y] < '0' || tab[x][y] > '9') && tab[x][y] != '.')
				return (0);
			y++;
		}
		if (y != 9)
			return (0);
		x++;
	}
	if (x != 9)
		return (0);
	return (1);
}
